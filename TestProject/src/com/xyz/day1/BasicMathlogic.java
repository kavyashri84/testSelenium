package com.xyz.day1;

public class BasicMathlogic {

	public int addNum(int num1,int num2){
		
		int sum = num1 + num2;
		return sum;
	
}
	public int subNum(int num1,int num2){
		
		int sum = num1 - num2;
		return sum;
	
}	public  int mplyNum(int num1,int num2){
		
		int sum = num1 * num2;
		return sum;
	
}
	public int divNum(int num1,int num2){
		
		int rem = num1 / num2;
		return rem;
	
}

}
